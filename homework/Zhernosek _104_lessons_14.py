""" функция для деления одного числа на второе"""


def division(a, b):
    try:  # проверяет верли следующее условие
        return a / b  # возвращает результат деления двух аргументов
    except ZeroDivisionError:  # исключение деления на ноль
        print("на ноль делить нельзя")  # сообщение об ошибке
        b = int(input("введите новое второе число не равное нулю >>> "))  # ввод нового числа
        return a / b  # возвращает результат деления двух аргументов


