"""Функция для определения являестся ли два слова анограмой"""


def word_anagram(b: str, a: str):  # объявление функции с двумя переменными
    list_1 = list(b.upper())  # приводим первую строку к верхнему регистру и типу стринг
    list_ = list(a.upper())  # приводим вторую строку к верхнему регистру и типу стринг
    list_.sort()  # сортируем первый лист
    list_1.sort()  # сортируем второй лист
    print(list_ == list_1)  # вывод значения сравнения двух листов


word_anagram('life', 'efil')  # вызов функции
word_anagram('life', 'ufoo')  # вызов функции


