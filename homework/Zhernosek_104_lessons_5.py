# таблица умножения
for i in range(1, 10):  # переменная i проводит итерацию с объектами рандж - это первый множитель
    for w in range(1, 10):  # переменная w проводит итерацию с объектами рандж - это второй множитель
        a = (i * w)  # операция перемножения
        y = (f"{w}*{i}={a}")  # формирование строки из переменных
        print('{0: <8}'.format(y), end=' ')  # форматирование и вывод строки
    print()

