    print(arr)  # вывод массива
    for a in arr:  # переменная i проводит итерацию с объектами массива arr
        if arr.count(a) != 1:  # сравнение количетво повторений одного объекта массива arr с 1
            arr_1.append(a)  # добавление  обектов из массива в массив которые втречаются 2 раза и более
            print(arr_1)  # вывод массива
